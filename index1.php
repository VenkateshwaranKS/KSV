<h2 style="color:blue">Design and Analysis of Algorithm</h2>
<p><span style="color:red">*</span>An algorithm, named after the ninth century scholar Abu Jafar Muhammad Ibn Musu Al-Khowarizmi, is defined as follows: Roughly speaking:</p>
<ol>
  <li>An algorithm is a set of rules for carrying out calculation either by hand or on a machine.</li>
  <br>
  <li> An algorithm is a finite step-by-step procedure to achieve a required result.</li>
<br>
  <li> An algorithm is a sequence of computational steps that transform the input into the output.</li>
<br>
  <li>  An algorithm is a sequence of operations performed on data that have to be organized in data structures.</li>
<br>
   <li> An algorithm is an abstraction of a program to be executed on a physical machine (model of Computation).</li>
</ol><br>
<p><span style="color:red">*</span>The most famous algorithm in history dates well before the time of the ancient Greeks: this is the Euclid's algorithm for calculating the greatest common divisor of two integers. This theorem appeared as the solution to the Proposition II in the Book VII of Euclid's "Elements." Euclid's "Elements" consists of thirteen books, which contain a total number of 465 propositions.</p>

