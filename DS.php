<h2 style="color:yellow">Data Structures</h2>
  <p><span style="color:red">*</span> Data Structures are the programmatic way of storing data so that data can be used efficiently.
   Almost every enterprise application uses various types of data structures in one or other way.
   This tutorial will give you great understanding on Data Structures concepts needed to understand the complexity of enterprise level applications and need of algorithms, data structures.</p>
<h3>Audience:</h3>
  <p><span style="color:red">*</span> This tutorial is designed for Computer Science graduates as well as Software Professionals who are willing to learn Data Structures and algorithm Programming in simple and easy steps. This tutorial will give you great understanding on Data Structures concepts and after completing this tutorial you will be at intermediate level of expertise from where you can take yourself at higher level of expertise.</p>
<h3>Prerequisites</h3>
<p><span style="color:red">*</span> Before proceeding with this tutorial you should have a basic understanding of C programming language, text editor and execution of programs etc.</p>
